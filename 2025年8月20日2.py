# 1. 重複を取り除く
# -----------------------------
tu = [10, 20, 30, 20, 40, 10]
unique_tu = list(dict.fromkeys(tu))  # 重複を排除し順序保持
print("ユニーク:", unique_tu)

# フローチャートコメント
# [開始] → [リスト tu を用意] → [dict.fromkeys で重複削除] → [list に変換] → [出力] → [終了]

# -----------------------------
# 2. 辞書をソート
# -----------------------------
book = {"apple": 100, "banana": 200, "orange": 150}

# キーでソート
sorted_keys = sorted(book.keys())
print("キー順:", sorted_keys)

# 値でソート
sorted_items = sorted(book.items(), key=lambda x: x[1])
print("値順:", sorted_items)

# フローチャートコメント
# [開始] → [辞書 book を用意] → [sorted() でキーソート] → [出力] → [sorted() で値ソート] → [出力] → [終了]

# -----------------------------
# 3. リストの引き算
# -----------------------------
v = [1,2,3,4]
b = [3,4,5,6]
diff = [x for x in v if x not in b]
print("v - b:", diff)

# フローチャートコメント
# [開始] → [リスト v と b を用意] → [内包表記で b にない要素を抽出] → [出力] → [終了]

# -----------------------------
# 4. タプルのリストから名前だけ抽出
# -----------------------------
data = [("Alice", 25), ("Bob", 30)]
names = [item[0] for item in data]
print("名前リスト:", names)

# フローチャートコメント
# [開始] → [タプルリスト data を用意] → [リスト内包表記で名前を抽出] → [出力] → [終了]

# -----------------------------
# 5. 辞書の値の合計
# -----------------------------
do = {"a": 1, "b": 2, "c": 3}
total = sum(do.values())
print("合計:", total)

# フローチャートコメント
# [開始] → [辞書 do を用意] → [values() で値を取得] → [sum() で合計] → [出力] → [終了]
